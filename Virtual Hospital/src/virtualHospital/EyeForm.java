    private boolean isvalidated() {
        if (!((Iritation.isSelected()) || (Etching.isSelected()) || (Watering.isSelected()) || (Redness.isSelected()) || (ScaringEyeLids.isSelected()) || (SwalingsMargins.isSelected()) || (Hemorrhage.isSelected()) || (COngestion.isSelected()) || (Odema.isSelected()) || (photophibia.isSelected()) || (Dimness.isSelected()) || (Nausea.isSelected()) || (GradualLossSugar.isSelected()) || (Burning.isSelected()) || (SeverePain.isSelected()) || (totalvisionloss.isSelected()) || (bluring.isSelected()) || (GrdaualLoss.isSelected()) || (swellingEyeLids.isSelected()))) {
            JOptionPane.showMessageDialog(null, "One of the symptom must be selected", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

