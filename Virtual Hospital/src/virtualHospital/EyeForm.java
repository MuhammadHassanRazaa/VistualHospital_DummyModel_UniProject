    public int GuessDisease() {
        int n = 0;
        if (Iritation.isSelected()) {
            if (Etching.isSelected()) {
                if (Watering.isSelected()) {
                    if (Burning.isSelected()) {
                        if (Redness.isSelected()) {
                            n = 0;
                        }
                    }
                    if (SeverePain.isSelected()) {
                        if (photophibia.isSelected()) {
                            n = 5;
                        }
                    }
                }
                if (ScaringEyeLids.isSelected()) {
                    n = 1;
                }
            }
            
        } else if (SwalingsMargins.isSelected()) {
            n = 2;
        } else if (swellingEyeLids.isSelected()) {
            n = 3;
        } else if (Hemorrhage.isSelected() && COngestion.isSelected() && Odema.isSelected()) {
            n = 4;
        }
        
        else   if (Dimness.isSelected()) {
            n = 6;
        }
        else   if (bluring.isSelected() && Nausea.isSelected() && Redness.isSelected() && SeverePain.isSelected()) {
            n = 7;
        }
        else if(GradualLossSugar.isSelected()){
            n=8;
        }
        else if(GrdaualLoss.isSelected()){
            n=9;
        }
        else if(totalvisionloss.isSelected()){
            n=10;
        }
    return n;
    }

