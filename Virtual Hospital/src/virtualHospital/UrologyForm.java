    public int GuessDisease() {
        int n = 0;
        if (Burning.isSelected() && FeverChills.isSelected() && CloudyUrine.isSelected()&& FeelingShaky.isSelected() ) {
            
                            n = 0;
                      
            
        } else if (AbdomenPain.isSelected() && PainInRIbs.isSelected()&& PainInUrination.isSelected()&& CloudyUrine.isSelected() && PinkRedBrownUrine.isSelected()&&Nausea.isSelected()&&Vomiting.isSelected()) {
            n = 1;
        } else if (PelvisPain.isSelected() && PainInUrination.isSelected() &&FrequentUrination.isSelected() &&Vomiting.isSelected()&&CloudyUrine.isSelected()) {
            n = 2;
        } else if (BloodInUrine.isSelected()&&PainInRIbs.isSelected()&&WeightLoss.isSelected()&&Fatigue.isSelected()&&FeverChills.isSelected()&&SwellingOfAnkles.isSelected()&&NightSweat.isSelected() ) {
            n = 3;
        }
        
       
        else n=0;
    return n;
    }

