    private boolean isvalidated() {
        if (!((Burning.isSelected()) || (CloudyUrine.isSelected()) || (FeelingShaky.isSelected()) || (FeverChills.isSelected()) || (PainInRIbs.isSelected()) || (AbdomenPain.isSelected()) || (PainInUrination.isSelected()) || (PinkRedBrownUrine.isSelected()) || (Nausea.isSelected()) || (Vomiting.isSelected()) || (PelvisPain.isSelected()) || (FrequentUrination.isSelected()) || (BloodInUrine.isSelected()) || (WeightLoss.isSelected()) || (Fatigue.isSelected()) || (SwellingOfAnkles.isSelected()) || (NightSweat.isSelected()))) {
            JOptionPane.showMessageDialog(null, "One of the symptom must be selected", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

