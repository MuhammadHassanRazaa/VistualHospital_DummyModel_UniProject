    private boolean isvalidated() {
        if (!((RedRash.isSelected()) || (Itching.isSelected()) || (ScalySkin.isSelected()) || (Bumps.isSelected()) || (Swelling.isSelected()) || (Burning.isSelected()) || (Redness.isSelected()) || (Cracks.isSelected()) || (Hives.isSelected()) || (OozingBlisters.isSelected()) || (SkinRedness.isSelected()) || (DaekenedSkin.isSelected()) || (Whiteheads.isSelected()) || (Blackheads.isSelected()) || (SoreNotHeal.isSelected()) || (SpreadPigments.isSelected()) )) {
            JOptionPane.showMessageDialog(null, "One of the symptom must be selected", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

