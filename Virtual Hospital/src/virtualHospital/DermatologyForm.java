    public int GuessDisease() {
        int n = 0;
        if (RedRash.isSelected() && Itching.isSelected() && ScalySkin.isSelected()&& Bumps.isSelected() &&Swelling.isSelected()&& Burning.isSelected() ) {
            
                            n = 0;
                      
            
        } else if (Redness.isSelected() && Cracks.isSelected()&& ScalySkin.isSelected() && Itching.isSelected()) {
            n = 1;
        } else if (OozingBlisters.isSelected() && Hives.isSelected() && Redness.isSelected()&&DaekenedSkin.isSelected()) {
            n = 2;
        } else if (Bumps.isSelected() &&Blackheads.isSelected()&&Whiteheads.isSelected() ) {
            n = 3;
        }
        
        else   if (SoreNotHeal.isSelected() && SpreadPigments.isSelected()) {
            n = 4;
        }
        
        else n=0;
    return n;
    }

