    private boolean isvalidated() {
        if (!((BreathingDifficulty.isSelected()) || (ChestPain.isSelected()) || (WeightLoss.isSelected()) || (Sweating.isSelected()) || (GeneralWeakness.isSelected()) || (BloodWithSputum.isSelected()) || (Flue.isSelected()) || (Fever.isSelected()) || (LungsSputum.isSelected()) || (MildCoughing.isSelected()))) {
            JOptionPane.showMessageDialog(null, "One of the symptom must be selected", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

