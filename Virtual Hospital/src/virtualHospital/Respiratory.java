    public int GuessDisease() {
        int n = 0;
        if (Fever.isSelected() && Flue.isSelected() && MildCoughing.isSelected()) {
            n=0;
            if(BreathingDifficulty.isSelected()){
                n=1;
            }
        } else if (Fever.isSelected() && BreathingDifficulty.isSelected() && MildCoughing.isSelected() && ChestPain.isSelected()) {
           n=2;
        } else if (GeneralWeakness.isSelected() && Sweating.isSelected() && WeightLoss.isSelected() && BloodWithSputum.isSelected()) {
            n = 3;
        } else if (BreathingDifficulty.isSelected()) {
            n = 4;
        } else if (ChestPain.isSelected() && Fever.isSelected() && MildCoughing.isSelected() && BreathingDifficulty.isSelected()) {
            n = 5;
        } else if (BloodWithSputum.isSelected()&&Fever.isSelected() && MildCoughing.isSelected() && ChestPain.isSelected()&& BreathingDifficulty.isSelected()) {
            n = 6;}
            else if (BreathingDifficulty.isSelected() && ChestPain.isSelected()&&MildCoughing.isSelected()) 
                { n = 7;
            }
         else if (LungsSputum.isSelected()&& MildCoughing.isSelected()&& BreathingDifficulty.isSelected()){
            n = 8;
        }
         else n=0;

        return n;
    }

