    private boolean isValidated() {
        if (NotinLIstRadioButton.isSelected()) {
            if (jTextField1.getText().trim().length() == 0) {
                JOptionPane.showMessageDialog(null, "Please Enter Your Desired Department", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().trim().contains(",")) {
                JOptionPane.showMessageDialog(null, "Please Enter Valid Department. Commas Not Allowed Here", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().equalsIgnoreCase("eye")) {
                JOptionPane.showMessageDialog(null, "Department Already in List ", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().equalsIgnoreCase("ent")) {
                JOptionPane.showMessageDialog(null, "Department Already in List ", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().equalsIgnoreCase("digestive")) {
                JOptionPane.showMessageDialog(null, "Department Already in List ", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().equalsIgnoreCase("respiratory")) {
                JOptionPane.showMessageDialog(null, "Department Already in List ", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().equalsIgnoreCase("brain")) {
                JOptionPane.showMessageDialog(null, "Department Already in List ", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().equalsIgnoreCase("cardiology") || jTextField1.getText().equalsIgnoreCase("heart")) {
                JOptionPane.showMessageDialog(null, "Department Already in List ", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().equalsIgnoreCase("Urology")) {
                JOptionPane.showMessageDialog(null, "Department Already in List ", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().equalsIgnoreCase("dermatology")) {
                JOptionPane.showMessageDialog(null, "Department Already in List ", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().equalsIgnoreCase("gynecology")) {
                JOptionPane.showMessageDialog(null, "Department Already in List ", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().equalsIgnoreCase("Skeletal")) {
                JOptionPane.showMessageDialog(null, "Department Already in List ", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }
            if (jTextField1.getText().equalsIgnoreCase("Muscles")) {
                JOptionPane.showMessageDialog(null, "Department Already in List ", "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
                jTextField1.setText("");
                return false;
            }

        }
        return true;
    }
