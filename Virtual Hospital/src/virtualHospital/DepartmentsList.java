    private void NextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NextButtonActionPerformed
        if (isValidated()) {

            if (EyeRadioButton.isSelected()) {
                new EyeForm(dataaa).setVisible(true);
                this.dispose();
            } else if (ENTRadioButton.isSelected()) {
                new ENTForm(dataaa).setVisible(true);
                this.dispose();
            } else if (DigestiveRadioButton.isSelected()) {
                new DigestiveForm(dataaa).setVisible(true);
                this.dispose();
            } else if (RespiratoryRadioButton.isSelected()) {
                new Respiratory(dataaa).setVisible(true);
                this.dispose();
            } else if (BrainRadioButton.isSelected()) {
                new BrainForm(dataaa).setVisible(true);
                this.dispose();
            } else if (CardiologyRadioButton.isSelected()) {
                new CardiologyForm(dataaa).setVisible(true);
                this.dispose();
            } else if (UrologyRadioButton.isSelected()) {
                new UrologyForm(dataaa).setVisible(true);
                this.dispose();
            } else if (DermatologyRadioButton.isSelected()) {
                new DermatologyForm(dataaa).setVisible(true);
                this.dispose();
            } else if (NotinLIstRadioButton.isSelected()) {
                JOptionPane.showMessageDialog(null, "We are Working on " + jTextField1.getText().trim() + " Department.\nSonn added here", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }

    }//GEN-LAST:event_NextButtonActionPerformed

