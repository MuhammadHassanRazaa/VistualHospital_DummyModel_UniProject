    public int GuessDisease() {
        int n = 0;
        if (Headache.isSelected() && Fever.isSelected() && JointPain.isSelected()&& Seizures.isSelected()&& PersonalityChange.isSelected() &&Confusion.isSelected()&& Drowsiness.isSelected() ) {
            
                            n = 0;
                      
            
        } else if (ChangeINSPeech.isSelected() && Confusion.isSelected()&& Fever.isSelected()&& Seizures.isSelected() && LossOfCoordination.isSelected()) {
            n = 1;
        } else if (ChangeINSPeech.isSelected() && SLowMovement.isSelected() ) {
            n = 2;
        } else if (LossOfConsciousness.isSelected() ) {
            n = 3;
        }
        
        else   if (WalkingDifficulty.isSelected() && Dizziness.isSelected()) {
            n = 4;
        }
        else   if (PoorDicision.isSelected() ) {
            n = 5;
        }
        else if(StiffNeck.isSelected()&& Confusion.isSelected()&&Irritability.isSelected()&&Headache.isSelected()&&Fever.isSelected()){
            n=8;
        }
        else n=0;
    return n;
    }

