    private boolean isvalidated() {
        if (!((Headache.isSelected()) || (JointPain.isSelected()) || (Seizures.isSelected()) || (PersonalityChange.isSelected()) || (Confusion.isSelected()) || (Drowsiness.isSelected()) || (ChangeINSPeech.isSelected()) || (LossOfCoordination.isSelected()) || (SLowMovement.isSelected()) || (LossOfConsciousness.isSelected()) || (WalkingDifficulty.isSelected()) || (Dizziness.isSelected()) || (PoorDicision.isSelected()) || (InvoluntryTwiching.isSelected()) || (StiffNeck.isSelected()) || (Irritability.isSelected()) || (MusclePain.isSelected()))) {
            JOptionPane.showMessageDialog(null, "One of the symptom must be selected", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

